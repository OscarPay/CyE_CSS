    private boolean contrasenaValida(String contrasena) {
        boolean contraCorrecta = false;
        if (contrasena.equals("")) {

            contraCorrecta = false;

        } else {

            String contrasenaVerificacion = JOptionPane.showInputDialog("Comprueba la Contrase√±a:");

            if (contrasena.equals(contrasenaVerificacion)) {
                contraCorrecta = true;
            } else {
                contraCorrecta = false;
            }

        }

        return contraCorrecta;
    }

